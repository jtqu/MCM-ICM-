%一次二次移动平均预测法
%%
x=[4.81 4.8 4.73  4.7 4.7 4.73 4.75 4.75 5.43 5.78 5.85 5.88 5.93];  %原始数据输入部分
yt=x'; n=length(yt);
for i=1:n-2
    m1(i+2)=(x(i)+x(i+1)+x(i+2))/3;     %一次移动平均值
end
for i=3:n-2
    m2(i+2)=(m1(i)+m1(i+1)+m1(i+2))/3;     %二次移动平均值
end
m1=m1';
m2=m2';
a=2*m1-m2;
b=m1-m2;
for i=1:6
    y(:,i)=a(n,1)+b(n,1)*i;
end
